        private static string[] _labels;

        // ドロップダウン表示用のラベル配列（インデックスは Entries に対応）。
        public static string[] Labels
        {
            get
            {
                if (_labels == null)
                {
                    _labels = new string[Entries.Length];
                    for (int i = 0; i < Entries.Length; i++)
                    {
                        _labels[i] = Entries[i].Label;
                    }
                }
                return _labels;
            }
        }

        // speaker ID に対応するドロップダウンのインデックス。未知の ID は 0（共通設定）にフォールバック。
        public static int IndexOf(int speakerId)
        {
            for (int i = 0; i < Entries.Length; i++)
            {
                if (Entries[i].Id == speakerId)
                {
                    return i;
                }
            }
            return 0;
        }

        // ドロップダウンのインデックスに対応する speaker ID。
        public static int IdAt(int index)
        {
            if (index < 0 || index >= Entries.Length)
            {
                return 0;
            }
            return Entries[index].Id;
        }
    }
